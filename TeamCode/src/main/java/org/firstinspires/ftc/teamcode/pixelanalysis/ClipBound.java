package org.firstinspires.ftc.teamcode.pixelanalysis;

public class ClipBound {
    
    public int left;
    public int top;
    public int width;
    public int height;

    public ClipBound(int left, int top, int width, int height) {
        this.left = left;
        this.top = top;
        this.width = width;
        this.height = height;
    }
}
